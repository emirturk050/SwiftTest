import Foundation

class MathematicFunc {
    
    func addInt(x: Int, y:Int) -> Int {
        return x + y
    }
    
    
    func multiplyInt(x: Int, y: Int) -> Int {
        return x * y
    }
    
   
    func divideInt(x: Int, y: Int) -> Int {
        return x / y
    }
}
